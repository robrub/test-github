let nome = "Mario";
let eta = 30;

// Usa i backtick per creare una stringa template
let saluto = `Ciao, il mio nome è ${nome} e ho ${eta} anni.`;

console.log(saluto); // Stampa "Ciao, il mio nome è Mario e ho 30 anni."

let base = 5;
let altezza = 10;

// Calcola l'area di un rettangolo all'interno di una stringa template
let area = `L'area del rettangolo è ${base * altezza}.`;

console.log(area); // Stampa "L'area del rettangolo è 50."